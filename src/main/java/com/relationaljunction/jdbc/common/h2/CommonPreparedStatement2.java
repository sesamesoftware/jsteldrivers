   public boolean execute() throws SQLException {
      checkClosed();

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "Connection(" + conn.toString() +
                      ") -> PreparedStatement(" +
                      this +
                      ") -> execute(): sql=" +
                      quotedSql);

      if (command.getType() == SQLCommand.SELECT ||
              command.getType() == SQLCommand.EXPLAIN) {
         executeQuery();
         return true;
      } else {
         executeUpdate();
         return false;
      }
   }

   public int[] executeBatch() throws SQLException {
      checkClosed();

      int[] result = null;

//    synchronized (conn.getH2Connection()) {

      conn.getCacheTableManager().writeLockTables(command.getTablesUsed());
      try {
         result = h2Stat.executeBatch();

         if (conn.autoCommit)
            conn.commit(command.getTablesUsed());
      } catch (Exception ex) {
         if (conn.getAutoCommit())
            conn.getCacheTableManager().clearTransactionFlag(command);
         log.error("Error while executing a prepared batch. " +
                 ex.getMessage(), ex);
         throw conn.driver.createException("Error while executing a prepared batch. " +
                 ex.getMessage(), ex);
      } finally {
         conn.getCacheTableManager().writeUnlockTables(command.getTablesUsed());
      }

      // it is not required to call clearBatch() here,
      // since the current batch is already cleared by H2

//    } // synchronized

      return result;
   }

   public ResultSet executeQuery() throws SQLException {
      checkClosed();

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "Connection(" + conn.toString() +
                      ") -> PreparedStatement(" +
                      this +
                      ") -> executeQuery(): sql=" +
                      quotedSql);

      //#######for registration###########
      r();
      //#################################

      if (command.getType() != SQLCommand.SELECT &&
              command.getType() != SQLCommand.EXPLAIN)
         throw conn.driver.createException(
                 "The executeQuery() method is only allowed for a query. " +
                         "Use execute() or executeUpdate() instead of executeQuery().");

//    synchronized (conn.getH2Connection()) {
      //#######for registration###########
      updateRecordsLoadedInfo();
      //#################################

      ResultSet rs = null;

      // lock tables for reading
      conn.cacheTableManager.readLockTables(realTablesUsed);

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log, "execute the query: " + quotedSql + " directly in H2");

      try {
         rs = ((JdbcPreparedStatement) h2Stat).executeQuery();

         // set a wrapper connection to allow updating a result set.
         if (getResultSetConcurrency() == ResultSet.CONCUR_UPDATABLE) {
            ((JdbcResultSet) rs).setWrapperConnection(conn);
         }
      } catch (SQLException ex) {
         throw conn.driver.createException(
                 "Error while executing an SQL query. [H2 Database] " + ex.getMessage(),
                 ex.getSQLState(), ex);
      } finally {
         // unlock tables for reading
         conn.cacheTableManager.readUnlockTables(realTablesUsed);
      }

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "end of executing the query: '" +
                      quotedSql +
                      "'");

      return rs;
//    } // synchronized
   }

   public int executeUpdate() throws SQLException {
      checkClosed();

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "Connection(" + conn.toString() +
                      ") -> PreparedStatement(" +
                      this +
                      ") -> executeUpdate(): sql=" +
                      quotedSql);

      //#######for registration###########
      r();
      //#################################

      int updateCount = 0;

      if (command.getType() == SQLCommand.SELECT ||
              command.getType() == SQLCommand.EXPLAIN)
         throw conn.driver.createException(
                 "This method is not allowed for a SELECT query. " +
                         "Use executeQuery() instead of executeUpdate() for SELECT queries.");

//    synchronized (conn.getH2Connection()) {
      //#######for registration###########
      updateRecordsLoadedInfo();
      //#################################

      conn.getCacheTableManager().writeLockTables(command.getTablesUsed());
      try {
         try {
            com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
                    "execute the prepared update directly in H2: '" +
                            quotedSql +
                            "'");

            updateCount = ((JdbcPreparedStatement) h2Stat).executeUpdate();
         } catch (SQLException ex) {
//	ex.printStackTrace();
            throw new SQLException("[H2 Database] " + ex.getMessage(),
                    ex.getSQLState());
         }

         if (conn.autoCommit)
            conn.commit(command.getTablesUsed());

//    } // synchronized

      } catch (Exception ex) {
         // error ocurred in a transaction

         if (conn.getAutoCommit()) {
            // just clear transaction flag for the table involved
            // rollback should be executed on the customer's side
            // if (autoCommit = false) this action is not required,
            // because a transaction goes in progress
            conn.getCacheTableManager().clearTransactionFlag(command);
         }

         log.error("Error while executing an SQL query. " +
                 ex.getMessage(), ex);
         throw conn.driver.createException("Error while executing an SQL query. " +
                 ex.getMessage(), ex);
      } finally {
         conn.getCacheTableManager().writeUnlockTables(command.getTablesUsed());
      }

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "end of executing the prepared update. Row processed " +
                      updateCount);

      return updateCount;
   }

