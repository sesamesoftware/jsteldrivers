   public CommonPreparedStatement2(CommonConnection2 conn,
                                   String sourceSql,
                                   int resultSetType,
                                   int resultSetConcurrency,
                                   int resultSetHoldability,
                                   int autoGeneratedKeys,
                                   int[] columnIndexes,
                                   String[] columnNames) throws SQLException {
      super(conn);

      // parse a sql to get tables that should be loaded to H2
      try {
         command = SQLCommand.parseSQLCommand(sourceSql);
      } catch (Exception ex) {
         throw new SQLException("Can't parse SQL query '" + sourceSql + "'. Error was " + ex.getMessage() +
                 ". Check the following points in your SQL syntax:\n" +
                 "1) column using an SQL reserved word as a name or containing spaces and other delimiters (-,.,;,:, etc) must be quoted in double quotes in a query, e.g.: SELECT \"DATE\", \"My integer-column\" FROM \"test.txt\"" + "\n" +
                 "2) To use single quotes (') within a string constant you should duplicate them, e.g.: SELECT 'a''bcd''efgh'", "42001");
      }

      com.relationaljunction.utils.OtherUtils.writeLogInfo(log,
              "preparing a statement for the query '" +
                      sourceSql + "'");

      this.quotedSql = command.getSqlText();
      realTablesUsed = loadTablesFromStore(command.getTablesUsed());

      if (autoGeneratedKeys != -1)
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql, autoGeneratedKeys);
      else if (columnIndexes != null)
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql, columnIndexes);
      else if (columnNames != null)
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql, columnNames);
      else if (resultSetHoldability != -1)
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql, resultSetType, resultSetConcurrency, resultSetHoldability);
      else if (resultSetConcurrency != -1)
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql, resultSetType, resultSetConcurrency);
      else
         h2Stat = conn.getH2Connection().prepareStatement(this.
                 quotedSql);

      init();

      ((JdbcPreparedStatement) h2Stat).setListener(this);
   }

